// Helper: extract unique users
export function getUsers(): KolUser[] {
  const userMap = new Map<string, KolUser>()
  for (const r of tweetResponses) {
    if (!userMap.has(r.user.id)) {
      userMap.set(r.user.id, r.user)
    }
  }
  return Array.from(userMap.values())
}

// Helper: extract tweets with userId attached
export function getTweets(): (TweetResponse["tweet"] & { userId: string })[] {
  return tweetResponses.map((r) => ({ ...r.tweet, userId: r.user.id }))
}

